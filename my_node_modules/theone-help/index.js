var fs = require('fs');
var mkdirp = require('mkdirp');

exports.autoPath = function  (path, cb) {
    fs.readdir(path, function  (err) {
        if(err){
            mkdirp(path,function  (err) {
                if(err){
                    cb(err);
                }
                cb();
            });
        }else{
            cb();
        }
    });
};




